<h1>Qu'es-ce que zDesigns.fr ?</h1>
<div class="bloc_texte">
    <b>zDesigns.fr</b> est un site dont l'intérêt est lié au célèbre <u>Site du Zéro</u>. Il est possible de créer des <strong>designs distants</strong> pour ce site,
    c'est à dire que n'importe qui peux créer un design qui s'adapte à la structure du <u>Site du Zéro</u> et le partager avec le reste de la communauté.
    <ul>
        <li>Ces designs distants sont appellés plus communément <strong>zDesigns</strong>, d'où le nom du site. Celui-ci <b>regroupe la plupart des zDesigns</b> permettant
            ainsi une vision globale, pour les membres du SdZ, des designs qu'ils peuvent essayer et adopter d'un simple clic.</li>
        <li>D'autre part, il est fournit à qui le souhaite un <b>espace d'hébergement de 1Mo / design</b>, ainsi qu'un outil développé, appellé <strong>zExplorer</strong>,
            pour <b>accompagner le designer dans la création puis la mise à jour</b> du design au fil des évolutions du Site du Zéro.</li>
    </ul>
    zDesigns.fr est l'initative de Cyril5789 (developpeur) & Kenny61, suivis pour la V2 par Alex-D (designer & developpeur). Ce site est totalement indépendant du SdZ.
